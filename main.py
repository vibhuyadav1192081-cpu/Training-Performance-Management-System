    "import os\n",
    "import glob\n",
    "import uuid\n",
    "import warnings\n",
    "import numpy as np\n",
    "import pandas as pd\n",
    "from datetime import datetime\n",
    "\n",
    "# Prevent terminal flooding\n",
    "warnings.filterwarnings(\"ignore\")\n",
    "\n",
    "def run_entire_pipeline():\n",
    "    \"\"\"\n",
    "    Automatically aggregates all CSV files from the 'data/' folder, \n",
    "    calculates metrics, triggers performance engine, updates the dashboard database,\n",
    "    and returns a clean master dataframe.\n",
    "    \"\"\"\n",
    "    DATA_FOLDER = \"data\"\n",
    "    OUTPUT_FOLDER = \"output\"\n",
    "    \n",
    "    # Ensure standard structural directories exist\n",
    "    os.makedirs(DATA_FOLDER, exist_ok=True)\n",
    "    os.makedirs(OUTPUT_FOLDER, exist_ok=True)\n",
    "    os.makedirs(os.path.join(OUTPUT_FOLDER, \"grade_cards\"), exist_ok=True)\n",
    "    os.makedirs(os.path.join(OUTPUT_FOLDER, \"reports\"), exist_ok=True)\n",
    "\n",
    "    csv_files = sorted(glob.glob(os.path.join(DATA_FOLDER, \"*.csv\")))\n",
